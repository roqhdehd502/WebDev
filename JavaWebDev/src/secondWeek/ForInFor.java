package secondWeek;

public class ForInFor {

	public static void main(String[] args) {
		for (int i = 0; i < 3; i++) {
			System.out.println("------------------");
			for (int j = 0; j < 3; j++) {
				System.out.println("[" + i + ", " + j + "]");
			}
		} 
		System.out.println("------------------");
	}

}
