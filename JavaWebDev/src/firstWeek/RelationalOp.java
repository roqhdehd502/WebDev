package firstWeek;

public class RelationalOp {

	public static void main(String[] args) {
		System.out.println("3 >= 2: " + (3 >= 2)); // 관계연산자의 타입은 boolean이다.
		System.out.println("3 <= 2: " + (3 <= 2));
		System.out.println("3 == 2: " + (3 == 2));
		System.out.println("3 != 2: " + (3 != 2));
	}

}
